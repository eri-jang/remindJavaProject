package com.bj.day07;

import java.util.Scanner;

public class WhileTest {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		do {
			int a = sc.nextInt();
			int b = sc.nextInt();
			System.out.println(a + "+" + b + "=" + a + b);
		}
		while(true);
			
		
		
	}

}
